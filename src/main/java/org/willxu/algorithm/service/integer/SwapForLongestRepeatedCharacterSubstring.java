package org.willxu.algorithm.service.integer;

public interface SwapForLongestRepeatedCharacterSubstring {
    /**
     * You are given a string text. You can swap two of the characters
     * in the text.
     * <p>
     * Return the length of the longest substring with repeated
     * characters.
     *
     * @param text 1 <= text.length <= 2 * 10^4
     *             consist of lowercase English characters only.
     */
    int maxRepOpt1(String text);
}
